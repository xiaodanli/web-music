<template>
    <div ref="wrapper">
        <slot></slot>
    </div>
</template>
<script>
    import BScroll from 'better-scroll'
    export default {
        props:{
            data:{
                type:Array,
                default:null
            },
            probeType:{
                type:Number,
                default:1
            },
            click:{
                type:Boolean,
                default:true
            }
        },
        mounted(){
            setTimeout(() => {
                this._initScroll()
            },20)
        },
        methods:{
            _initScroll(){
                if(!this.$refs.wrapper){
                    return
                }
                this.scroll = new BScroll(this.$refs.wrapper,{
                    probeType:1,
                    click:this.click
                })
            },
            enable(){
                this.scroll && this.scroll.enable()
            },
            disable(){
                this.scroll && this.scroll.disable()
            },
            refresh(){
                this.scroll && this.scroll.refresh()
            }
        },
        watch:{
            data(){
                setTimeout(()=>{
                    this.refresh()
                },20)
            }
        }
    }
</script>